-- Width of side windows
M.width = function()
    local columns = vim.go.columns
    return math.floor(columns * 0.2) > 25 and math.floor(columns * 0.2) or 25
end

local append_space = function(icons)
    local result = {}
    for k, v in pairs(icons) do
        result[k] = v .. ' '
    end
    return result
end

